const CACHE_COLORDATA: ObjectMap<ColorData> = {};

const CHAR_HEX = '0123456789ABCDEF';
const REGEXP_HEX = /[A-Za-z\d]{3,}/;
const REGEXP_RGBA = /rgba?\((\d+), (\d+), (\d+)(?:, ([\d.]+))?\)/;

const parseOpacity = (value: string) => parseFloat(value.trim() || '1') * 255;

export function findColorName(value: string) {
    for (const color of X11_CSS3) {
        if (color.name === value.toLowerCase()) {
            return color;
        }
    }
    return undefined;
}

export function findColorShade(value: string) {
    const rgba = convertRGBA(value);
    if (rgba) {
        const hsl = convertHSLA(rgba);
        const result: ColorResult[] = [];
        let baseline = -1;
        for (let i = 0; i < X11_CSS3.length; i++) {
            const color = X11_CSS3[i];
            if (color.value === value) {
                return color;
            }
            else if (baseline !== -1) {
                if (baseline === color.hsl.h) {
                    result.push(color);
                }
            }
            else if (hsl.h >= color.hsl.h) {
                result.push(color);
                baseline = color.hsl.h;
            }
        }
        if (result.length === 1) {
            return result[0];
        }
        else if (result.length > 1) {
            const total = hsl.l + hsl.s;
            const combined: number[] = [];
            for (const color of result) {
                combined.push(Math.abs(total - (color.hsl.l + color.hsl.s)));
            }
            let nearest = Number.POSITIVE_INFINITY;
            let index = -1;
            for (let i = 0; i < combined.length; i++) {
                if (combined[i] < nearest) {
                    nearest = combined[i];
                    index = i;
                }
            }
            return result[index];
        }
        else {
            return X11_CSS3[X11_CSS3.length - 1];
        }
    }
    return undefined;
}

export function parseColor(value: string, opacity = '1', transparency = false) {
    if (value && (value !== 'transparent' || transparency)) {
        if (CACHE_COLORDATA[value]) {
            return CACHE_COLORDATA[value];
        }
        let name = '';
        let rgba: RGBA | undefined;
        if (value.charAt(0) === '#') {
            rgba = convertRGBA(value);
        }
        else if (value.startsWith('rgb')) {
            const match = value.match(REGEXP_RGBA);
            if (match) {
                rgba = {
                    r: parseInt(match[1]),
                    g: parseInt(match[2]),
                    b: parseInt(match[3]),
                    a: match[4] ? parseFloat(match[4]) : parseOpacity(opacity)
                };
            }
        }
        else {
            switch (value) {
                case 'transparent':
                    rgba = { r: 0, g: 0, b: 0, a: 0 };
                    name = 'transparent';
                    break;
                case 'initial':
                    rgba = { r: 0, g: 0, b: 0, a: 255 };
                    name = 'black';
                    break;
                default:
                    const color = findColorName(value);
                    if (color) {
                        rgba = {
                            ...color.rgb,
                            a: parseOpacity(opacity)
                        };
                        name = value;
                    }
                    break;
            }
        }
        if (rgba && (rgba.a > 0 || transparency)) {
            const hexAsString = getHexCode(rgba.r, rgba.g, rgba.b);
            const alphaAsString = getHexCode(rgba.a);
            const alpha = rgba.a / 255;
            CACHE_COLORDATA[value] = <ColorData> {
                name,
                value: `#${hexAsString}`,
                valueAsRGBA: `#${hexAsString + alphaAsString}`,
                valueAsARGB: `#${alphaAsString + hexAsString}`,
                rgba,
                hsl: convertHSLA(rgba),
                alpha,
                opaque: alpha > 0 && alpha < 1,
                transparent: alpha === 0
            };
            Object.freeze(CACHE_COLORDATA[value]);
            return CACHE_COLORDATA[value];
        }
    }
    return undefined;
}

export function reduceColor(value: string, percent: number) {
    const rgba = convertRGBA(value);
    if (rgba) {
        const base = percent < 0 ? 0 : 255;
        percent = Math.abs(percent);
        return parseColor(
            formatRGBA({
                r: (rgba.r + Math.round((base - rgba.r) * percent)) % 255,
                g: (rgba.g + Math.round((base - rgba.g) * percent)) % 255,
                b: (rgba.b + Math.round((base - rgba.b) * percent)) % 255,
                a: rgba.a
            })
        );
    }
    return undefined;
}

export function getHexCode(...values: number[]) {
    let output = '';
    for (const value of values) {
        const rgb = Math.max(0, Math.min(value, 255));
        if (isNaN(rgb)) {
            output += '00';
        }
        else {
            output += CHAR_HEX.charAt((rgb - (rgb % 16)) / 16) + CHAR_HEX.charAt(rgb % 16);
        }
    }
    return output;
}

export function convertHex(value: RGBA) {
    return `#${getHexCode(value.r, value.g, value.b) + (value.a < 255 ? getHexCode(value.a) : '')}`;
}

export function convertRGBA(value: string) {
    value = value.replace(/#/g, '').trim();
    if (REGEXP_HEX.test(value)) {
        let a = 255;
        switch (value.length) {
            case 4:
                a = parseInt(value.charAt(3).repeat(2), 16);
            case 3:
                value = value.charAt(0).repeat(2) + value.charAt(1).repeat(2) + value.charAt(2).repeat(2);
                break;
            case 5:
                value += value.charAt(4);
                break;
            default:
                if (value.length >= 8) {
                    a = parseInt(value.substring(6, 8), 16);
                }
                value = value.substring(0, 6);
                break;
        }
        if (value.length === 6) {
            return <RGBA> {
                r: parseInt(value.substring(0, 2), 16),
                g: parseInt(value.substring(2, 4), 16),
                b: parseInt(value.substring(4), 16),
                a
            };
        }
    }
    return undefined;
}

export function convertHSLA(value: RGBA) {
    const r = value.r / 255;
    const g = value.g / 255;
    const b = value.b / 255;
    const min = Math.min(r, g, b);
    const max = Math.max(r, g, b);
    let h = (max + min) / 2;
    let s = h;
    const l = h;
    if (max === min) {
        h = 0;
        s = 0;
    }
    else {
        const d = max - min;
        s = l > 0.5 ? d / (2 - max - min) : d / (max + min);
        switch (max) {
            case r:
                h = (g - b) / d + (g < b ? 6 : 0);
                break;
            case g:
                h = (b - r) / d + 2;
                break;
            case b:
                h = (r - g) / d + 4;
                break;
        }
        h /= 6;
    }
    return <HSLA> {
        h: Math.round(h * 360),
        s: Math.round(s * 100),
        l: Math.round(l * 100),
        a: value.a / 255
    };
}

export function formatRGBA(value: RGBA) {
    return `rgb${value.a < 255 ? 'a' : ''}(${value.r}, ${value.g}, ${value.b}${value.a < 255 ? `, ${(value.a / 255).toPrecision(2)}` : ''})`;
}

export function formatHSLA(value: HSLA) {
    return `hsl${value.a < 255 ? 'a' : ''}(${value.h}, ${value.s}%, ${value.l}%${value.a < 255 ? `, ${(value.a / 255).toPrecision(2)}` : ''})`;
}